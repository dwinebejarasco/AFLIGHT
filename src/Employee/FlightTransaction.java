    public void logEvent(int userId, String username, String userType) {
    dbConnect dbc = new dbConnect();
    Connection con = dbc.getConnection();
    PreparedStatement pstmt = null;
    String ut = "Active"; // Assuming the status is "Active" once logged in

    try {
        String sql = "INSERT INTO tbl_logs (u_id, u_username, login_time, u_type, log_status) VALUES (?, ?, ?, ?, ?)";
        pstmt = con.prepareStatement(sql);

        pstmt.setInt(1, userId);
        pstmt.setString(2, username);
        pstmt.setTimestamp(3, new Timestamp(new Date().getTime())); // Log the current time
        pstmt.setString(4, userType);
        pstmt.setString(5, ut);

        pstmt.executeUpdate();
        System.out.println("Login log recorded successfully.");
    } catch (SQLException e) {
        System.out.println("Error recording log: " + e.getMessage());
    } finally {
        try {
            if (pstmt != null) pstmt.close();
            if (con != null) con.close();
        } catch (SQLException e) {
            System.out.println("Error closing resources: " + e.getMessage());
        }
    }
}

 private void handleSubmit() {
    String Departure = dep.getText().toString();
     String Arrival = arrival.getText().toString();

    // Get how many hours user wants to park (e.g., 1, 2, 3 from JComboBox)
  // Get selected ticket quantity (from a combo box or input)
int ticketQuantity = Integer.parseInt(qty.getSelectedItem().toString()); // 'qty' is your JComboBox or input for ticket quantity
int ratePerTicket = 3000; // Set your ticket price
int totalPayment = ticketQuantity * ratePerTicket;

// Format the date
String dateStr = yy.getSelectedItem().toString() + "-" +
                 String.format("%02d", mm.getSelectedIndex() + 1) + "-" +
                 String.format("%02d", Integer.parseInt(dd.getSelectedItem().toString()));

try {
    Connection conn = DriverManager.getConnection("jdbc:mysql://localhost/flights", "root", "");

    // Get user info from session
    Session sess = Session.getInstance();
    int userId = sess.getUid();
    String username = sess.getUname();

    String sql = "INSERT INTO flight(u_id, departure, arrival, payment, date, time, status) " +
                 "VALUES (?, ?, ?, ?, ?, ?, ?);";

    PreparedStatement stmt = conn.prepareStatement(sql);
    stmt.setInt(1, userId);
    stmt.setString(2, Departure);
    stmt.setString(3, Arrival);                        
    stmt.setString(4, String.valueOf(totalPayment));
    stmt.setDate(5, java.sql.Date.valueOf(dateStr));
    stmt.setTime(6, java.sql.Time.valueOf("00:00:00")); // No specific time, just dummy placeholder
    stmt.setString(7, "Pending");

    stmt.executeUpdate();
JOptionPane.showMessageDialog(this,
    "Reservation submitted.\nDeparture: " + Departure +
    "\nArrival: " + Arrival +
    "\nTickets: " + ticketQuantity +
    "\nTotal Payment: â‚±" + totalPayment);


    logEvent(userId, username, "Submitted a reservation for " + ticketQuantity + " ticket(s).");

    conn.close();
    this.dispose();

} catch (Exception e) {
    e.printStackTrace();
    JOptionPane.showMessageDialog(this, "Error: " + e.getMessage());
}
 }



